package bean;

/**
 *
 * @author Marcelo
 */
public class Cliente extends CodeBase{
    private String nome;
    private String cpf;
    private String cnpj;
    private String endereco;

    public Cliente() {
    }

    public Cliente(String nome, String cpf, String cnpj, String endereco) {
        this.nome = nome;
        this.cpf = cpf;
        this.cnpj = cnpj;
        this.endereco = endereco;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }
    
    
}
